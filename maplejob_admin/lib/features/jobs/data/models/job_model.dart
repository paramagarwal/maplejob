import 'package:cloud_firestore/cloud_firestore.dart';
import '../../domain/entities/job_entity.dart';

class JobModel extends JobEntity {
  const JobModel({
    required super.id,
    required super.title,
    required super.description,
    required super.requirements,
    required super.salary,
    required super.experience,
    required super.employmentType,
    required super.vacancyCount,
    required super.city,
    required super.state,
    required super.country,
    required super.officeLocation,
    required super.workMode,
    required super.benefits,
    required super.requiredSkills,
    super.applicationDeadline,
    required super.minimumQualification,
    required super.preferredQualification,
    required super.department,
    required super.category,
    required super.createdAt,
    required super.updatedAt,
    required super.isActive,
  });

  factory JobModel.fromMap(Map<String, dynamic> map, String id) {
    return JobModel(
      id: id,
      title: map['title'] as String? ?? '',
      description: map['description'] as String? ?? '',
      requirements: map['requirements'] as String? ?? '',
      salary: map['salary'] as String? ?? '',
      experience: map['experience'] as String? ?? '',
      employmentType: map['employmentType'] as String? ?? '',
      vacancyCount: map['vacancyCount'] as int? ?? 0,
      city: map['city'] as String? ?? '',
      state: map['state'] as String? ?? '',
      country: map['country'] as String? ?? '',
      officeLocation: map['officeLocation'] as String? ?? '',
      workMode: map['workMode'] as String? ?? '',
      benefits: map['benefits'] as String? ?? '',
      requiredSkills: List<String>.from(map['requiredSkills'] as List? ?? []),
      applicationDeadline: (map['applicationDeadline'] as Timestamp?)?.toDate(),
      minimumQualification: map['minimumQualification'] as String? ?? '',
      preferredQualification: map['preferredQualification'] as String? ?? '',
      department: map['department'] as String? ?? '',
      category: map['category'] as String? ?? '',
      createdAt: (map['createdAt'] as Timestamp?)?.toDate() ?? DateTime.now(),
      updatedAt: (map['updatedAt'] as Timestamp?)?.toDate() ?? DateTime.now(),
      isActive: map['isActive'] as bool? ?? true,
    );
  }

  Map<String, dynamic> toMap() {
    return {
      'title': title,
      'description': description,
      'requirements': requirements,
      'salary': salary,
      'experience': experience,
      'employmentType': employmentType,
      'vacancyCount': vacancyCount,
      'city': city,
      'state': state,
      'country': country,
      'officeLocation': officeLocation,
      'workMode': workMode,
      'benefits': benefits,
      'requiredSkills': requiredSkills,
      'applicationDeadline': applicationDeadline != null ? Timestamp.fromDate(applicationDeadline!) : null,
      'minimumQualification': minimumQualification,
      'preferredQualification': preferredQualification,
      'department': department,
      'category': category,
      'createdAt': Timestamp.fromDate(createdAt),
      'updatedAt': Timestamp.fromDate(updatedAt),
      'isActive': isActive,
    };
  }

  JobModel copyWith({
    String? title,
    String? description,
    String? requirements,
    String? salary,
    String? experience,
    String? employmentType,
    int? vacancyCount,
    String? city,
    String? state,
    String? country,
    String? officeLocation,
    String? workMode,
    String? benefits,
    List<String>? requiredSkills,
    DateTime? applicationDeadline,
    String? minimumQualification,
    String? preferredQualification,
    String? department,
    String? category,
    DateTime? createdAt,
    DateTime? updatedAt,
    bool? isActive,
  }) {
    return JobModel(
      id: id,
      title: title ?? this.title,
      description: description ?? this.description,
      requirements: requirements ?? this.requirements,
      salary: salary ?? this.salary,
      experience: experience ?? this.experience,
      employmentType: employmentType ?? this.employmentType,
      vacancyCount: vacancyCount ?? this.vacancyCount,
      city: city ?? this.city,
      state: state ?? this.state,
      country: country ?? this.country,
      officeLocation: officeLocation ?? this.officeLocation,
      workMode: workMode ?? this.workMode,
      benefits: benefits ?? this.benefits,
      requiredSkills: requiredSkills ?? this.requiredSkills,
      applicationDeadline: applicationDeadline ?? this.applicationDeadline,
      minimumQualification: minimumQualification ?? this.minimumQualification,
      preferredQualification: preferredQualification ?? this.preferredQualification,
      department: department ?? this.department,
      category: category ?? this.category,
      createdAt: createdAt ?? this.createdAt,
      updatedAt: updatedAt ?? this.updatedAt,
      isActive: isActive ?? this.isActive,
    );
  }
}
